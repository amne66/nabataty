import { collection, onSnapshot } from "firebase/firestore";
import { db } from "../firebaseSetup";

export interface Iplant {
	id: string;
    name: string;
	price: number;
	watering: string;
	lighting: string;
	temperature: string;
	description: string;
    imageUrl:string;
}
interface InPlantProps {
  data: Iplant; 
  id:String;
}

 export let iPlants: InPlantProps[] = []

const indoorPlantRef = collection(db, "indoorPlant");
onSnapshot(indoorPlantRef, (querySnapshot:any) => {
    iPlants=(querySnapshot.docs.map((doc: { id: any; data: () => any; }) => ({
      id: doc.id,
      data: doc.data() 
    })))
  })

