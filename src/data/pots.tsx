import { collection, onSnapshot } from "firebase/firestore";
import { db } from "../firebaseSetup";

export interface IPots {
	id: string;
    name: string;
	price: number;
	description: string;
    imageUrl:string;
}
interface IPOtProps {
	data: IPots;
    id : string;
}
export let potsArr: IPOtProps[]  = [];
const potRef = collection(db, "post");
onSnapshot(potRef, (querySnapshot:any) => {
    potsArr=(querySnapshot.docs.map((doc: { id: any; data: () => any; }) => ({
      id: doc.id,
      data: doc.data() 
    })))
  })


