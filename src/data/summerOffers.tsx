import { collection, onSnapshot } from "firebase/firestore";
import { db } from "../firebaseSetup";


export interface IOffer {
	id: string;
    name: string;
	oldPrice: number;
    newPrice:number;
	watering: string;
	lighting: string;
	temperature: string;
	description: string;
    imageUrl:string;
}
interface IOfferstProps {
	data: IOffer;
  id:string;
}
export let iOffers: IOfferstProps[] = [];
const offerRef = collection(db, "summerOffers");
onSnapshot(offerRef, (querySnapshot:any) => {
    iOffers=(querySnapshot.docs.map((doc: { id: any; data: () => any; }) => ({
      id: doc.id,
      data: doc.data() 
    })))
  })


